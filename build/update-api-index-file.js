/**
 * 生成api文件
 */
const fs = require('fs');
const render = require('json-templater/string');
const path = require('path');
const endOfLine = require('os').EOL;
const apis = require('./utils').getApis();
const uppercamelcase = require('uppercamelcase');

apis.unshift('common');
const OUTPUT_PATH = path.join(__dirname, '../src/api/index.js');
const IMPORT_TEMPLATE = 'import {{name}} from \'./{{package}}\';';
const MAIN_TEMPLATE = `/* Automatically generated by './build/update-api-index-file.js' */
{{include}}

export default {
{{list}}
};
`;

const includeComponentTemplate = [];
const listTemplate = [];

apis.forEach((name) => {
  let componentName = uppercamelcase(name);
  componentName = componentName.replace(/^\S/, char => char.toLowerCase());

  includeComponentTemplate.push(render(IMPORT_TEMPLATE, {
    name: componentName,
    package: name
  }));

  if (name === 'common') {
    listTemplate.push(`  ...${ componentName }`);
  } else {
    listTemplate.push(`  ${ componentName }`);
  }
});

const template = render(MAIN_TEMPLATE, {
  include: includeComponentTemplate.join(endOfLine),
  list: listTemplate.join(`,${ endOfLine }`)
});

fs.writeFileSync(OUTPUT_PATH, template);
console.log('[update api index file] DONE:\n', OUTPUT_PATH);
